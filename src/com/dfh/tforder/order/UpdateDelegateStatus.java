package com.dfh.tforder.order;

public class UpdateDelegateStatus {

	private String Version;
	private String MsgTime;
	private String MsgId;
	private String Msg;
	private String Orderidx;
	private String Status;
	private String CompleteNum;
	private String CancelNum;
	private String Price;

	public String getVersion() {
		return Version;
	}

	public void setVersion(String version) {
		Version = version;
	}

	public String getMsgTime() {
		return MsgTime;
	}

	public void setMsgTime(String msgTime) {
		MsgTime = msgTime;
	}

	public String getMsgId() {
		return MsgId;
	}

	public void setMsgId(String msgId) {
		MsgId = msgId;
	}

	public String getMsg() {
		return Msg;
	}

	public void setMsg(String msg) {
		Msg = msg;
	}

	public String getOrderidx() {
		return Orderidx;
	}

	public void setOrderidx(String orderidx) {
		Orderidx = orderidx;
	}

	public String getStatus() {
		return Status;
	}

	public void setStatus(String status) {
		Status = status;
	}

	public String getCompleteNum() {
		return CompleteNum;
	}

	public void setCompleteNum(String completeNum) {
		CompleteNum = completeNum;
	}

	public String getCancelNum() {
		return CancelNum;
	}

	public void setCancelNum(String cancelNum) {
		CancelNum = cancelNum;
	}

	public String getPrice() {
		return Price;
	}

	public void setPrice(String price) {
		Price = price;
	}

}
